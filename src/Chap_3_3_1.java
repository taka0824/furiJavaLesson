public class Chap_3_3_1 {
    public static void main(String[] args) {
        for(int cnt = 1; cnt <= 5; cnt ++){
            System.out.println(cnt + "回目のハロー");
        }
        System.out.println("------------------");
        for(int cnt = 5; cnt >= 1; cnt --){
            System.out.println(cnt + "回目のハロー");
        }
    }
}
